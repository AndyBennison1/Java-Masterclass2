public class DaysOfWeek {


    public static void main(String[] args) {

        int DayOfWeek = 71;
        switch (DayOfWeek) {
            case 1:
                System.out.println("Monday");
                break;

            case 2:
                System.out.println("Tuesday");
                break;

            case 4:
                System.out.println("Thursday");

            default:
                System.out.println("We don't know");
        }
    }
}
